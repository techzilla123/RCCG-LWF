"use client"; // Client component directive

import localFont from "next/font/local";
import Navbar from "@/components/Navbar";
import { ReactQueryClientProvider } from "@/providers";
import { usePathname } from "next/navigation";
import "./globals.css";

const geistSans = localFont({
  src: "./fonts/GeistVF.woff",
  variable: "--font-geist-sans",
  weight: "100 900", // Use an array for the weight
});
const geistMono = localFont({
  src: "./fonts/GeistMonoVF.woff",
  variable: "--font-geist-mono",
  weight: "100 900", // Use an array for the weight
});

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const pathname = usePathname();

  // Define paths where Navbar should not be displayed
  const hideNavbarPaths = [
    "/dashboard/dashboard-overview",
    "/dashboard/transactions",
    "/dashboard/settings",
    "/dashboard/users",
    "/dashboard/payments",
    "/admin",
  ];

  const shouldShowNavbar = !hideNavbarPaths.some((path) =>
    pathname.startsWith(path)
  );

  return (
    <html lang="en">
      <ReactQueryClientProvider>
        {/* Use Metadata API in Next.js 13+ */}
        <head>
          <title>YCT PAYMENT GATEWAY</title>
          <meta name="description" content="Generated by YCT-Payment Gateway" />
          <meta name="viewport" content="width=device-width, initial-scale=1" />
          <meta charSet="UTF-8" />
          <meta name="author" content="Techzilla" />
          <meta name="keywords" content="YCT, Payment, Dashboard, Admin" />
          
          {/* Add the favicon link */}
          <link rel="icon" href="/Vector.png" type="image/png" />
        </head>
        <body
          className={`${geistSans.variable} ${geistMono.variable} antialiased`}
        >
          {shouldShowNavbar && <Navbar />}
          {children}
        </body>
      </ReactQueryClientProvider>
    </html>
  );
}
