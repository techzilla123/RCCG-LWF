"use client"; // Client component directive

import localFont from "next/font/local";
import Navbar from "@/components/Navbar";
import { ReactQueryClientProvider } from "@/providers";
import { usePathname } from "next/navigation";
import Head from "next/head"; // Importing Head component
import "./globals.css";

const geistSans = localFont({
  src: "./fonts/GeistVF.woff",
  variable: "--font-geist-sans",
  weight: "100 900",
});
const geistMono = localFont({
  src: "./fonts/GeistMonoVF.woff",
  variable: "--font-geist-mono",
  weight: "100 900",
});

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const pathname = usePathname();

  // Define paths where Navbar should not be displayed
  const hideNavbarPaths = ["/dashboard/dashboard-overview", "/admin"];
  const shouldShowNavbar = !hideNavbarPaths.some((path) =>
    pathname.startsWith(path)
  );

  return (
    <html lang="en">
      <ReactQueryClientProvider>
        <Head>
          <title>Create Next App</title>
          <meta name="description" content="Generated by create next app" />
          <meta name="viewport" content="width=device-width, initial-scale=1" />
          <meta charSet="UTF-8" />
          <meta name="author" content="Techzilla" />
          <meta name="keywords" content="YCT, Payment, Dashboard, Admin" />
        </Head>
        <body
          className={`${geistSans.variable} ${geistMono.variable} antialiased`}
        >
          {shouldShowNavbar && <Navbar />}
          {children}
        </body>
      </ReactQueryClientProvider>
    </html>
  );
}
